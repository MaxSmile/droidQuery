package self.philbrown.droidQuery;

/**
 * Data types for <em>ajax</em> request responses
 * @author Phil Brown
 * @since 10:45:11 AM, Jul 2, 2014
 *
 */
public enum DataType
{
	/** JavaScript Object Notation */
	JSON, 
	/** Extensible Markup Language */
	XML, 
	/** Textual response */
	TEXT, 
	/** Bourne Script response*/
	SCRIPT, 
	/** Bitmap response */
	IMAGE
};